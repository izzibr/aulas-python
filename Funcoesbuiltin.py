# abs() retorna o valor obsoluto de um número
a = -10 
resultado = abs(a)
print (resultado)

# min() retorna o valor mínimo de uma sequência
menorPreco = min (6,7,4,8,9,10)
print (menorPreco)

# max() retorna p valor máximo de uma sequência 
maiorPreco = max (6,7,4,8,9,10)
print (maiorPreco)

# sum() retorna a soma dos valores de uma sequência 
somaTotal = sum ([1,2,3])
print (somaTotal)

# pow() exponenciação 
resultado = pow (2,3)
print (resultado)

# input () lê um valor digitado no teclado pelo usuário 
nome = input ("Qual o seu nome?")
print (nome)

