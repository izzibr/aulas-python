# gerar tabuada de multiplicação 

tabuada = int(input("Qual tabuada sera gerada?"))
limite = int (input("Até qual valor iremos calcular?"))
for numero in range(0, limite+1):
    print (f'{tabuada}x{numero}={tabuada*numero}')



